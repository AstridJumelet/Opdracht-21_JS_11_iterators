console.log("** iterators OLD style: while loop **");

let selectedColorsArray = ["yellow", "blue", "red", "orange"];
let i=0;
while (i < selectedColorsArray.length) {
    console.log(selectedColorsArray[i]);
    i++;
}

console.log("** iterators old style: for loop**");
let selectedColors1Array = ["yellow", "blue", "red", "orange"];
for (i = 0; i<selectedColors1Array.length; i++) {
    console.log(selectedColors1Array[i]);
}
